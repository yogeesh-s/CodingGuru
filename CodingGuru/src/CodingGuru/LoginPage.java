           if(!"".equals(str1))
            {
                if(!"".equals(str2))
                {
                    if(str1.equals(uname))
                    {
                        if(str2.equals(upass))
                        {
                                    this.setVisible(false);
                                    this.dispose();
                                    HomePage hp = new HomePage();
                                    hp.setVisible(true);
                                    
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Invalid Password");  
                        }
                                            
                    }
                    else if(!str2.equals(upass))
                    {
                        JOptionPane.showMessageDialog(null, "Invalid User Name and Password");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Invalid User Name");
                    }
                }
                else
                    JOptionPane.showMessageDialog(null, "Please Enter Password");
            }
            else if("".equals(str2))
            {
                JOptionPane.showMessageDialog(null, "Please Enter User Name and Password");
            }
            else
                JOptionPane.showMessageDialog(null, "Please Enter User Name");
            
