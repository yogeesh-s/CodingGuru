    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            
            
        
            fname = Rfname.getText();
            lname = Rlname.getText();
            email = Remail.getText();
            username = Rusername.getText();
            password = new String(Rpassword.getPassword());
            cpassword = new String(Rcpassword.getPassword());
            mobile = Rmnumber.getText();
            address = Raddress.getText();
            
            if(!"".equals(fname)&&!"".equals(lname)&&!"".equals(email)
                    &&!"".equals(username)&&!"".equals(password)
                    &&!"".equals(cpassword)&&!"".equals(mobile)
                    &&!"".equals(address))
            {
                    if(password.equals(cpassword))
                    {
                        this.setVisible(false);
                        this.dispose();
                        LoginPage lp = new LoginPage();
                        lp.setVisible(true);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Password Miss Match");
                    }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "All fields are mandatory");
            }
            
    }//GEN-LAST:event_jButton1ActionPerformed

